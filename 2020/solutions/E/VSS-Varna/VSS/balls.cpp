#include<iostream>
using namespace std;
int main ()
{
    int n,t,b,d=0,a=1,c=2;
    cin >> n >> t;
    for (int i=1;i<t;i++)
    {
        if (d=t)
            cout << t;
        if (a=t)
            cout << t;
        if (c=t)
            cout << t;
        if (d!=t)
            cout << d;
        if (c!=t)
            cout << c;
        if (a!=t)
            cout << a;
            b=n+t;
    }
    cout << b <<endl;
    return 0;
    }
