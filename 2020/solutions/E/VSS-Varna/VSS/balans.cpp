int main ()
{
    int a,b,d,c,n,br=0;
    cin >> br >> n;
    for (int i;i>br;i++)
    {
        if (br=a)
            cout << br;
        if (br=b)
            cout << br;
        if (br=b)
            cout << br;
        if (br=c)
            cout << br;
        if (br!=c)
            cout << c;
        if (br!=d)
            cout << d;
        if (br!=b)
            cout << b;
        if (br!=a)
            cout << a;
        if (a+b==c+d)
            cout << n;
        if (a+b!=c+d)
            cout << br;
    }
    cout << br <<endl;
    return 0;
}


